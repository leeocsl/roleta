function hashId(id: string) {
  return [...id].reduce((hash, character) => hash + character.charCodeAt(0), 0)
}

export function getChampionTheme(champion: Pick<ChampionData, 'id' | 'regions'>): ChampionTheme {
  const override = championOverrides[champion.id]
  if (override) return override

  const base = regionBaseColors[champion.regions[0] ?? 'runeterra']
  const offset = (hashId(champion.id) % 18) - 9

  return {
    background: `color-mix(in srgb, ${base} ${82 + Math.abs(offset)}%, ${offset >= 0 ? '#ffffff' : '#111827'})`,
    foreground: '#ffffff',
    shadow: 'dark',
  }
}
